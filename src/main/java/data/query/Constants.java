	public static int getConstantIndex(String constant, String[] constantArray) {
		int i = 0;
		for (String s : constantArray) {
			if (s.toLowerCase().equals(constant.toLowerCase())) {
				return i;
			}
			i++;
		}
		return DEFAULT_VALUE;
	}
	
